package integrador;

public class Pontos {
	private int recebidos;
	private int gastos;
	
	
	public int getRecebidos() {
		return recebidos;
	}
	public void setRecebidos(int recebidos) {
		this.recebidos = recebidos;
	}
	public int getGastos() {
		return gastos;
	}
	public void setGastos(int gastos) {
		this.gastos = gastos;
	}
	
	
}
